    let cartData = JSON.parse(localStorage.getItem("cart")) || [];
    let container=document.getElementById("menucont");
    function displayData(Menu){
    container.innerHTML=""
    Menu.forEach(product => {
            let card = document.createElement("div")      
            let img = document.createElement("img");
            let name = document.createElement("h2");
            let description = document.createElement("p");
            let category = document.createElement("h4");
            let price = document.createElement("h3");
            let addTocart = document.createElement("button");
    
            img.src = product.product_image;
            name.innerText = product.name;
            price.innerText = `₹${product.price}`;
            description.innerText = product.description;
            
            addTocart.innerText = "Order Now";
    
            addTocart.addEventListener("click",function(){
              if (checkAvalible(product)) {
                alert("Product Already in Cart");
              } else {
                cartData.push({ ...product, quantity: 1 });
    
                localStorage.setItem("cart", JSON.stringify(cartData));
                alert("Product Added To Cart");
              }
            })
    
            card.append(img, name, price, description, addTocart);
            container.append(card);
    
    });
    }
    
    function checkAvalible(product) {
          for (let i = 0; i < cartData.length; i++) {
            if (product.id === cartData[i].id) {
              return true;
            }
          }
          return false;
        }
    
    displayData(Menu)
    